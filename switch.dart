//Esta sentencia sirve para crear muchas condiciones de igualdad
void main(List<String> args) {
  int numero = 2;
  switch (numero) {
    case 1:
      print('Numero 1');
      break;
    case 2:
      print('Numero 2');
      break;
    case 3:
      print('Numero 3');
      break;
    default:
      print('Hola Mundo');
      break;
  }
}
